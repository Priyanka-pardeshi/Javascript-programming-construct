function CheckDay(number)
{
    if(number==1){
        console.log("Sunday");
    }
    else if(number==2)
    {
        console.log("Mon");
    }
    else if(number==3){
        console.log("Tue");
    }
    else if(number==4)
    {
        console.log("Wed");
    }
    else if(number==5){
        console.log("Thu");
    }
    else if(number==6)
    {
        console.log("fri");
    }
    else if(number==7){
        console.log("Sat");
    }
    else{
        console.log("Invalid day");
    }
}
CheckDay(4);